function TabContent({
  tab,
  idea,
  sections,
  onRun,
  onRunSection,
  isRunning,
}: {
  tab: TabKey;
  idea: Idea;
  sections: ReturnType<typeof useAiPipeline>["sections"];
  onRun: () => void;
  onRunSection: (key: SectionKey) => void;
  isRunning: boolean;
}) {
  if (tab === "overview") {
    return <OverviewSection idea={idea} sections={sections} onRun={onRun} isRunning={isRunning} />;
  }

  const sectionMap: Record<Exclude<TabKey, "overview">, React.ReactNode> = {
    problems:      <ProblemsSection        {...sections.problems}        />,
    customers:     <CustomersSection       {...sections.customers}       />,
    competition:   <CompetitionSection     {...sections.competition}     />,
    market:        <MarketPotentialSection {...sections.marketPotential} />,
    strategy:      <IdeaStrategySection    {...sections.ideaStrategy}    />,
    businessModel: <BusinessModelSection   {...sections.businessModel}   />,
    functions:     <FunctionsListSection   {...sections.functionsList}   />,
    mvp:           <MvpPlanningSection     {...sections.mvpPlanning}     />,
    financial:     <UnitEconomicsSection   {...sections.unitEconomics}   />,
    goToMarket:    <GoToMarketSection      {...sections.goToMarket}      />,
  };

  const stateKey = TAB_STATE_KEY[tab as Exclude<TabKey, "overview">];
  const s = sections[stateKey] as SectionState;

  if (!s.data && !s.isLoading) {
    const sectionKey = TAB_SECTION_KEY[tab as Exclude<TabKey, "overview">];
    const handleRun = sectionKey ? () => onRunSection(sectionKey) : onRun;
    return <SectionCallToAction onRun={handleRun} isRunning={isRunning} />;
  }

  return <>{sectionMap[tab as Exclude<TabKey, "overview">]}</>;
}

