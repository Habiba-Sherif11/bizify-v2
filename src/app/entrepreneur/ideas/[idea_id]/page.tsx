// ─── Page ─────────────────────────────────────────────────────────────────────

export default function IdeaDetailPage({
  params,
}: {
  params: Promise<{ idea_id: string }>;
}) {
  const { idea_id } = use(params);
  const [idea, setIdea]     = useState<Idea | null>(null);
  const [loading, setLoading] = useState(true);
  const [fetchError, setFetchError] = useState<string | null>(null);
  const [activeTab, setActiveTab] = useState<TabKey>("overview");
  const [lastActiveInGroup, setLastActiveInGroup] = useState<Record<string, TabKey>>({
    Setup: "overview",
    Insights: "problems",
    Planning: "businessModel",
  });
  const activeGroup = TAB_GROUPS.find(g => g.keys.includes(activeTab)) ?? TAB_GROUPS[0];
  const handleTabChange = useCallback((key: TabKey) => {
    const grp = TAB_GROUPS.find(g => g.keys.includes(key));
    if (grp) setLastActiveInGroup(prev => ({ ...prev, [grp.label]: key }));
    setActiveTab(key);
  }, []);
  const handleGroupClick = useCallback((grp: typeof TAB_GROUPS[0]) => {
    handleTabChange(lastActiveInGroup[grp.label] ?? grp.keys[0]);
  }, [lastActiveInGroup, handleTabChange]);

  const { sections, isRunning, hasRun, runError, runPipeline, runSection, regenerateSection, regenerateSectionCustom, fetchAll } = useAiPipeline(idea_id);

  const [customPromptOpen, setCustomPromptOpen]       = useState(false);
  const [customPromptSection, setCustomPromptSection] = useState<SectionKey | null>(null);
  const [customPromptText, setCustomPromptText]       = useState("");

  const [chatPopupOpen, setChatPopupOpen]       = useState(false);
  const [chatPopupSection, setChatPopupSection] = useState<SectionKey | null>(null);

  const [editModalOpen, setEditModalOpen] = useState(false);

  const [shareItems, setShareItems] = useState<ShareItem[] | null>(null);
  const [isSharing, setIsSharing]   = useState(false);

  const handleShare = useCallback(async () => {
    setIsSharing(true);
    try {
      const { data } = await api.post<{ items: ShareItem[] }>("/ideas/share", {
        idea_ids: [idea_id],
      });
      setShareItems(data.items);
    } catch {
      alert("Failed to create share link. Please try again.");
    } finally {
      setIsSharing(false);
    }
  }, [idea_id]);

  const [rediscoverLoading, setRediscoverLoading] = useState(false);
  const [rediscoverError, setRediscoverError]     = useState<string | null>(null);

  const handleRediscoverProblems = async () => {
    setRediscoverLoading(true);
    setRediscoverError(null);
    try {
      await api.post("/ai/rerun/problems", {}, { timeout: 120_000 });
      await fetchAll();
    } catch {
      setRediscoverError("Failed to re-discover problems. Please try again.");
    } finally {
      setRediscoverLoading(false);
    }
  };

  const handleDownloadPDF = () => {
    if (!idea) return;
    const html = buildFullAnalysisPdfHtml(idea, sections);
    const win = window.open("", "_blank");
    if (!win) return;
    win.document.open();
    win.document.write(html);
    win.document.close();
    win.focus();
    setTimeout(() => win.print(), 400);
  };

  useEffect(() => {
    api
      .get<Idea>(`/ideas/${idea_id}`)
      .then(({ data }) => setIdea(data))
      .catch(() => setFetchError("Failed to load idea."))
      .finally(() => setLoading(false));
  }, [idea_id]);

  // ── Loading / error states ──────────────────────────────────────────────────

  if (loading) {
    return (
      <div className="min-h-screen bg-neutral-100 dark:bg-neutral-900 flex items-center justify-center">
        <div className="w-6 h-6 rounded-full border-2 border-amber-400 border-t-transparent animate-spin" />
      </div>
    );
  }

  if (fetchError || !idea) {
    return (
      <div className="min-h-screen bg-neutral-100 dark:bg-neutral-900 flex items-center justify-center gap-3 text-red-500">
        <AlertCircle size={18} />
        <p className="text-sm">{fetchError ?? "Idea not found."}</p>
      </div>
    );
  }

  // ── Render ──────────────────────────────────────────────────────────────────

  return (
    <div className="min-h-screen bg-neutral-100 dark:bg-neutral-900 transition-colors">
      <main className="max-w-6xl mx-auto px-4 sm:px-6 lg:px-8 pb-16">

        {/* Breadcrumb */}
        <nav className="flex items-center gap-1.5 pt-6 pb-4 text-sm text-gray-500 dark:text-gray-400">
          <Link href="/entrepreneur" className="hover:text-gray-700 dark:hover:text-gray-200 transition-colors">
            <Home size={14} />
          </Link>
          <ChevronRight size={12} className="text-gray-400" />
          <Link href="/entrepreneur/ideas" className="hover:text-gray-700 dark:hover:text-gray-200 transition-colors">
            Ideas
          </Link>
          <ChevronRight size={12} className="text-gray-400" />
          <span className="text-gray-700 dark:text-gray-200 font-medium truncate max-w-[200px]">
            {idea.title ?? "Untitled idea"}
          </span>
        </nav>

        {/* Header row */}
        <div className="flex flex-col sm:flex-row sm:items-start sm:justify-between gap-4 mb-8">
          {/* Title + date */}
          <div className="flex flex-col gap-2">
            <h1 className="text-2xl sm:text-3xl font-bold text-foreground">{idea.title ?? "Untitled idea"}</h1>
            <p className="text-sm text-muted-foreground">{formatIdeaDate(idea.created_at)}</p>
          </div>

          {/* Actions */}
          <div className="flex items-center gap-2 shrink-0 flex-wrap">
            {/* Pipeline status */}
            {isRunning && (
              <span className="flex items-center gap-1.5 text-xs text-amber-600 dark:text-amber-400 font-medium">
                <Loader2 size={12} className="animate-spin" />
                Running pipeline…
              </span>
            )}
            {hasRun && !isRunning && (
              <span className="text-xs text-green-600 dark:text-green-400 font-medium">
                ✓ Pipeline complete
              </span>
            )}

            {/* Save as PDF + Share */}
            <div className="flex items-center gap-0.5">
              <SectionIconButton tooltip="Save as PDF" onClick={handleDownloadPDF}>
                <FileDown size={18} className="text-neutral-400" />
              </SectionIconButton>
              <SectionIconButton tooltip="Share" onClick={handleShare} disabled={isSharing}>
                {isSharing ? <Loader2 size={18} className="animate-spin text-neutral-400" /> : <Share2 size={18} className="text-neutral-400" />}
              </SectionIconButton>
            </div>

            {/* Edit with AI */}
            <button
              type="button"
              onClick={() => setEditModalOpen(true)}
              disabled={isRunning}
              className={cn(
                "flex items-center gap-2 px-4 py-2 rounded-lg text-sm font-medium text-white cursor-pointer transition-opacity",
                "bg-gradient-to-r from-amber-500 to-yellow-500 shadow-[0_2px_16px_rgba(255,183,3,0.3)] outline outline-[0.66px] outline-yellow-500/60",
                isRunning && "opacity-60 cursor-not-allowed"
              )}
            >
              <Sparkles size={14} />
              Edit with AI
            </button>
          </div>
        </div>

        {runError && (
          <p className="mb-4 text-sm text-red-500 bg-red-50 dark:bg-red-900/20 px-3 py-2 rounded-lg">
            {runError}
          </p>
        )}

        {/* Tab bar + section action icons */}
        {(() => {
          const activeSectionKey = activeTab !== "overview"
            ? TAB_STATE_KEY[activeTab as Exclude<TabKey, "overview">]
            : null;
          const activeSectionHasData = activeSectionKey ? !!sections[activeSectionKey].data : false;
          const activeSectionLoading = activeSectionKey ? sections[activeSectionKey].isLoading : false;

          return (
            <>
              {/* ── Tab bar (single horizontal scrollable row) ──────────────── */}
              <div className="sticky top-0 z-10 mb-6 py-2 bg-neutral-100 dark:bg-neutral-900 -mx-4 px-4 sm:-mx-6 sm:px-6 lg:-mx-8 lg:px-8">
                <div className={cn(
                  "px-2 py-1.5 bg-gray-200 dark:bg-neutral-800 rounded-lg flex items-center gap-2 overflow-x-auto",
                  "[&::-webkit-scrollbar]:h-1.5",
                  "[&::-webkit-scrollbar-track]:bg-transparent",
                  "[&::-webkit-scrollbar-thumb]:rounded-full",
                  "[&::-webkit-scrollbar-thumb]:bg-amber-400/70",
                  "[&::-webkit-scrollbar-thumb:hover]:bg-amber-500",
                )}>
                  {TABS.map(({ key, label }) => {
                    const isActive = activeTab === key;
                    const stateKey = key !== "overview"
                      ? TAB_STATE_KEY[key as Exclude<TabKey, "overview">]
                      : null;
                    const hasData   = stateKey ? !!sections[stateKey].data    : false;
                    const isLoading = stateKey ? sections[stateKey].isLoading : false;
                    return (
                      <button
                        key={key}
                        type="button"
                        onClick={() => setActiveTab(key)}
                        className={cn(
                          "shrink-0 min-h-10 px-4 py-2 rounded-xl text-sm font-medium transition-all cursor-pointer whitespace-nowrap flex items-center gap-1.5",
                          isActive
                            ? "bg-white dark:bg-neutral-700 text-foreground shadow-sm border border-neutral-300 dark:border-neutral-600"
                            : "text-neutral-600 dark:text-neutral-400 hover:bg-white/50 dark:hover:bg-neutral-700/50"
                        )}
                      >
                        {label}
                        {isLoading ? (
                          <span className="w-1.5 h-1.5 rounded-full bg-amber-400 animate-pulse shrink-0" />
                        ) : hasData ? (
                          <span className="w-1.5 h-1.5 rounded-full bg-emerald-500 shrink-0" />
                        ) : null}
                      </button>
                    );
                  })}
                </div>
              </div>

              {/* ── Section header card — title + action icons ───────────────── */}
              {activeSectionHasData && activeSectionKey && activeTab !== "overview" && TAB_SECTION_KEY[activeTab as Exclude<TabKey, "overview">] !== null && (
                <div className="flex items-center justify-between px-4 py-2.5 mb-4 bg-card border border-border rounded-xl">
                  <span className="text-sm font-semibold text-foreground">
                    {TABS.find(t => t.key === activeTab)?.label}
                  </span>
                  <div className="flex items-center gap-0.5">
                    <SectionIconButton
                      tooltip="Regenerate"
                      disabled={activeSectionLoading}
                      onClick={() => regenerateSection(activeSectionKey)}
                    >
                      <RefreshCw size={16} />
                    </SectionIconButton>
                    <SectionIconButton
                      tooltip="Regenerate with custom prompt"
                      disabled={activeSectionLoading}
                      onClick={() => {
                        setCustomPromptSection(activeSectionKey);
                        setCustomPromptText("");
                        setCustomPromptOpen(true);
                      }}
                    >
                      <Wand2 size={16} />
                    </SectionIconButton>
                    <SectionIconButton
                      tooltip="Chat with AI"
                      onClick={() => { setChatPopupSection(activeSectionKey); setChatPopupOpen(true); }}
                    >
                      <MessageCircle size={16} />
                    </SectionIconButton>
                  </div>
                </div>
              )}

              {/* Problems tab: Re-discover button */}
              {activeTab === "problems" && sections.problems.data && (
                <div className="flex items-center justify-between px-4 py-2.5 mb-4 bg-card border border-border rounded-xl">
                  <span className="text-sm font-semibold text-foreground">Problems</span>
                  <SectionIconButton
                    tooltip="Re-discover problems for this idea"
                    disabled={rediscoverLoading}
                    onClick={handleRediscoverProblems}
                  >
                    {rediscoverLoading ? <Loader2 size={16} className="animate-spin" /> : <RefreshCw size={16} />}
                  </SectionIconButton>
                </div>
              )}
              {rediscoverError && activeTab === "problems" && (
                <p className="mb-2 text-xs text-red-500">{rediscoverError}</p>
              )}
            </>
          );
        })()}

        {/* Custom prompt modal */}
        {customPromptOpen && (
          <div
            className="fixed inset-0 bg-black/40 flex items-center justify-center z-50"
            onClick={() => setCustomPromptOpen(false)}
          >
            <div
              className="bg-white dark:bg-neutral-800 rounded-2xl shadow-xl p-6 w-full max-w-md mx-4 flex flex-col gap-4"
              onClick={(e) => e.stopPropagation()}
            >
              <div className="flex items-center justify-between">
                <h3 className="text-sm font-semibold text-foreground">Custom Regeneration</h3>
                <button
                  type="button"
                  onClick={() => setCustomPromptOpen(false)}
                  className="p-1 rounded-lg hover:bg-neutral-100 dark:hover:bg-neutral-700 transition-colors cursor-pointer"
                >
                  <X size={15} className="text-neutral-500" />
                </button>
              </div>
              <textarea
                value={customPromptText}
                onChange={(e) => setCustomPromptText(e.target.value)}
                placeholder="Describe what you want to change or focus on…"
                rows={4}
                className="w-full text-sm border border-border rounded-xl p-3 resize-none bg-transparent focus:outline-none focus:ring-2 focus:ring-amber-400 text-foreground placeholder:text-muted-foreground"
                autoFocus
              />
              <button
                type="button"
                disabled={!customPromptText.trim()}
                onClick={() => {
                  if (customPromptSection && customPromptText.trim()) {
                    regenerateSectionCustom(customPromptSection, customPromptText.trim());
                    setCustomPromptOpen(false);
                    setCustomPromptText("");
                  }
                }}
                className={cn(
                  "flex items-center justify-center gap-2 px-4 py-2.5 rounded-xl text-sm font-medium text-white transition-opacity cursor-pointer",
                  "bg-gradient-to-r from-amber-500 to-yellow-500 shadow-[0_2px_12px_rgba(255,183,3,0.3)]",
                  !customPromptText.trim() && "opacity-50 cursor-not-allowed"
                )}
              >
                <Wand2 size={14} />
                Generate
              </button>
            </div>
          </div>
        )}

        {/* Tab content */}
        <TabContent
          tab={activeTab}
          idea={idea}
          sections={sections}
          onRun={runPipeline}
          onRunSection={runSection}
          isRunning={isRunning}
        />
      </main>

      {/* Section chat popup */}
      {chatPopupOpen && chatPopupSection && (
        <SectionChatPopup
          key={chatPopupSection}
          sectionKey={chatPopupSection}
          ideaId={idea_id}
          onClose={() => setChatPopupOpen(false)}
        />
      )}

      {/* Edit Idea with AI modal */}
      {editModalOpen && idea && (
        <IdeaEditChatModal
          idea={idea}
          ideaId={idea_id}
          onClose={() => setEditModalOpen(false)}
          onSectionsReset={() => { fetchAll(); }}
        />
      )}

      {shareItems && (
        <ShareModal items={shareItems} onClose={() => setShareItems(null)} />
      )}
    </div>
  );
}

function SectionIconButton({
  tooltip,
  onClick,
  disabled,
  children,
}: {
  tooltip: string;
  onClick: () => void;
  disabled?: boolean;
  children: React.ReactNode;
}) {
  return (
    <TooltipProvider>
      <Tooltip>
        <TooltipTrigger asChild>
          <button
            type="button"
            onClick={onClick}
            disabled={disabled}
            className={cn(
              "p-2 rounded-lg hover:bg-neutral-200 dark:hover:bg-neutral-700 transition-colors cursor-pointer text-neutral-500 dark:text-neutral-400",
              disabled && "opacity-40 cursor-not-allowed"
            )}
          >
            {children}
          </button>
        </TooltipTrigger>
        <TooltipContent>{tooltip}</TooltipContent>
      </Tooltip>
    </TooltipProvider>
  );
}
