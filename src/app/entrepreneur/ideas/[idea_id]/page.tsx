  customers:       "Customers",
  competition:     "Competition",
  marketPotential: "Market",
  ideaStrategy:    "Strategy",
  businessModel:   "Business Model",
  functionsList:   "Functions",
  mvpPlanning:     "MVP",
  unitEconomics:   "Financial",
  goToMarket:      "Go-to-Market",
  problems:        "Problems",
