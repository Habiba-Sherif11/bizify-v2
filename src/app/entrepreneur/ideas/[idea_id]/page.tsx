interface ChatMessage {
  role: "user" | "assistant";
  content: string;
}

function SectionChatPopup({
  sectionKey,
  ideaId,
  onClose,
}: {
  sectionKey: SectionKey;
  ideaId: string;
  onClose: () => void;
}) {
  const [messages, setMessages] = useState<ChatMessage[]>([]);
  const [inputValue, setInputValue] = useState("");
  const [isStreaming, setIsStreaming] = useState(false);
  const [sessionId, setSessionId] = useState<string | null>(null);
  const [loadingSession, setLoadingSession] = useState(true);
  const messagesEndRef = useRef<HTMLDivElement>(null);
  const abortRef = useRef<AbortController | null>(null);
  const slug = SECTION_CHAT_SLUG[sectionKey];
  const label = SECTION_LABEL[sectionKey] ?? sectionKey;

  // Find the latest existing session for this section+idea, or create one
  useEffect(() => {
    let cancelled = false;
    async function initSession() {
      setLoadingSession(true);
      try {
        // Look for an existing session for this section + idea
        const params = new URLSearchParams();
        if (slug) params.set("section_slug", slug);
        if (ideaId) params.set("idea_id", ideaId);
        const existing = await api.get(`/chat/sessions?${params.toString()}`);
        const sessions: Array<{ id: string }> = existing.data;

        let sid: string;
        if (sessions.length > 0) {
          // Reuse the most recent session
          sid = sessions[0].id;
        } else {
          // Create a new session
          const res = await api.post("/chat/sessions", {
            section_slug: slug,
            idea_id: ideaId || undefined,
            title: `${label} Chat`,
          });
          sid = res.data.id;
        }

        if (cancelled) return;
        setSessionId(sid);

        // Load last 10 messages
        const msgsRes = await api.get(`/chat/sessions/${sid}/messages?limit=10`);
        if (cancelled) return;
        const loaded: ChatMessage[] = (msgsRes.data as Array<{ role: string; content: string }>).map((m) => ({
          role: m.role as "user" | "assistant",
          content: m.content,
        }));
        setMessages(loaded);
      } catch {
        // Non-fatal: popup works without persistence
      } finally {
        if (!cancelled) setLoadingSession(false);
      }
    }
    initSession();
    return () => { cancelled = true; };
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  useEffect(() => {
    messagesEndRef.current?.scrollIntoView({ behavior: "smooth" });
  }, [messages]);

  const sendMessage = async () => {
    const text = inputValue.trim();
    if (!text || isStreaming || !slug) return;

    const history = messages;
    setInputValue("");
    setMessages((prev) => [...prev, { role: "user", content: text }]);
    setIsStreaming(true);
    setMessages((prev) => [...prev, { role: "assistant", content: "" }]);

    const ctrl = new AbortController();
    abortRef.current = ctrl;
    let assistantText = "";

    try {
      const response = await fetch(`/api/ai/${slug}/chat/stream`, {
        method: "POST",
        credentials: "include",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          message: text,
          history: history.map((m) => ({ role: m.role, content: m.content })),
          idea_id: ideaId || undefined,
        }),
        signal: ctrl.signal,
      });

      if (!response.ok || !response.body) throw new Error("Stream failed");

      const reader = response.body.getReader();
      const decoder = new TextDecoder();
      let buffer = "";

      while (true) {
        const { done, value } = await reader.read();
        if (done) break;
        buffer += decoder.decode(value, { stream: true });
        const lines = buffer.split("\n");
        buffer = lines.pop() ?? "";

        for (const line of lines) {
          if (!line.startsWith("data: ")) continue;
          const jsonStr = line.slice(6).trim();
          if (!jsonStr || jsonStr === "[DONE]") continue;
          try {
            const parsed = JSON.parse(jsonStr);
            if (parsed.type === "token" && parsed.content) {
              assistantText += parsed.content;
              setMessages((prev) => {
                const copy = [...prev];
                copy[copy.length - 1] = { role: "assistant", content: assistantText };
                return copy;
              });
            }
          } catch { /* ignore malformed chunks */ }
        }
      }

      // Save both messages to DB session
      if (sessionId && assistantText) {
        try {
          await api.post(`/chat/sessions/${sessionId}/messages`, {
            messages: [
              { role: "user", content: text },
              { role: "assistant", content: assistantText },
            ],
          });
        } catch {
          // Non-fatal: messages displayed locally even if save fails
        }
      }
    } catch (err) {
      if ((err as Error).name === "AbortError") return;
      setMessages((prev) => {
        const copy = [...prev];
        copy[copy.length - 1] = { role: "assistant", content: "Sorry, something went wrong. Please try again." };
        return copy;
      });
    } finally {
      setIsStreaming(false);
      abortRef.current = null;
    }
  };

  const handleKeyDown = (e: React.KeyboardEvent<HTMLTextAreaElement>) => {
    if (e.key === "Enter" && !e.shiftKey) {
      e.preventDefault();
      sendMessage();
    }
  };

  return (
    <div className="fixed bottom-6 right-6 z-50 flex flex-col w-[360px] max-w-[calc(100vw-2rem)] h-[500px] max-h-[calc(100vh-5rem)] bg-white dark:bg-neutral-900 rounded-2xl shadow-2xl border border-neutral-200 dark:border-neutral-700 overflow-hidden">
      {/* Header */}
      <div className="flex items-center justify-between px-4 py-3 border-b border-neutral-200 dark:border-neutral-700 bg-gradient-to-r from-amber-500 to-yellow-500 rounded-t-2xl">
        <div className="flex items-center gap-2">
          <MessageCircle size={15} className="text-white" />
          <span className="text-sm font-semibold text-white">{label} — AI Chat</span>
        </div>
        <div className="flex items-center gap-1">
          {sessionId && (
            <a
              href={`/entrepreneur/ai-chat?session_id=${sessionId}`}
              target="_blank"
              rel="noopener noreferrer"
              className="text-[10px] text-white/80 hover:text-white underline underline-offset-2 transition-colors px-1"
            >
              View full chat
            </a>
          )}
          <button
            type="button"
            onClick={onClose}
            className="p-1 rounded-lg hover:bg-white/20 transition-colors cursor-pointer"
          >
            <X size={15} className="text-white" />
          </button>
        </div>
      </div>

      {/* Messages */}
      <div className="flex-1 overflow-y-auto px-4 py-3 flex flex-col gap-3">
        {loadingSession && (
          <div className="flex items-center justify-center mt-4">
            <Loader2 size={16} className="animate-spin text-amber-400" />
          </div>
        )}
        {!loadingSession && messages.length === 0 && (
          <p className="text-xs text-muted-foreground text-center mt-4">
            Ask anything about the {label} analysis.
          </p>
        )}
        {messages.map((msg, i) => (
          <div
            key={i}
            className={cn(
              "max-w-[85%] px-3 py-2 rounded-xl text-sm leading-relaxed whitespace-pre-wrap",
              msg.role === "user"
                ? "self-end bg-amber-500 text-white rounded-br-sm"
                : "self-start bg-neutral-100 dark:bg-neutral-800 text-foreground rounded-bl-sm"
            )}
          >
            {msg.content}
            {msg.role === "assistant" && msg.content === "" && isStreaming && (
              <span className="inline-block w-1.5 h-3.5 bg-neutral-400 dark:bg-neutral-500 rounded-sm animate-pulse ml-0.5" />
            )}
          </div>
        ))}
        <div ref={messagesEndRef} />
      </div>

      {/* Input */}
      <div className="border-t border-neutral-200 dark:border-neutral-700 px-3 py-2.5 flex items-end gap-2">
        <textarea
          value={inputValue}
          onChange={(e) => setInputValue(e.target.value)}
          onKeyDown={handleKeyDown}
          placeholder="Ask about this section…"
          rows={1}
          disabled={isStreaming || loadingSession}
          className="flex-1 resize-none text-sm bg-neutral-100 dark:bg-neutral-800 border-0 rounded-xl px-3 py-2 focus:outline-none focus:ring-2 focus:ring-amber-400 text-foreground placeholder:text-muted-foreground max-h-28 overflow-y-auto"
          style={{ lineHeight: "1.4" }}
        />
        <button
          type="button"
          onClick={sendMessage}
          disabled={isStreaming || !inputValue.trim() || loadingSession}
          className={cn(
            "p-2.5 rounded-xl bg-amber-500 text-white flex-shrink-0 transition-opacity cursor-pointer",
            (isStreaming || !inputValue.trim() || loadingSession) && "opacity-40 cursor-not-allowed"
          )}
        >
          {isStreaming
            ? <Loader2 size={15} className="animate-spin" />
            : <Send size={15} />}
        </button>
      </div>
    </div>
  );
}

// ─── PDF export ───────────────────────────────────────────────────────────────

function esc(s: unknown): string {
  return String(s ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function safeParseJson(raw: string | null): Record<string, unknown> | null {
  if (!raw) return null;
  try {
    const parsed = JSON.parse(raw);
    return parsed && typeof parsed === "object" ? parsed : null;
  } catch {
    return null;
  }
}

// ─── Smart generic renderer for unknown JSON shapes ───────────────────────────

function smartRenderValue(value: unknown, depth = 0): string {
  if (value === null || value === undefined || value === "") return "";
  if (typeof value === "boolean") {
    return `<span class="pdf-bool ${value ? "bool-yes" : "bool-no"}">${value ? "✓ Yes" : "✗ No"}</span>`;
  }
  if (typeof value === "number") return `<span class="pdf-num">${value.toLocaleString()}</span>`;
  if (typeof value === "string") {
    if (!value.trim()) return "";
    return `<p class="pdf-text">${esc(value)}</p>`;
  }
  if (Array.isArray(value)) {
    const items = value.filter((v) => v !== null && v !== undefined && v !== "");
    if (!items.length) return "";
    if (items.every((v) => typeof v === "string" || typeof v === "number")) {
      return `<ul class="pdf-list">${items.map((v) => `<li>${esc(v)}</li>`).join("")}</ul>`;
    }
    return `<div class="pdf-item-list">${items.map((v) => `<div class="pdf-item-block">${smartRenderValue(v, depth + 1)}</div>`).join("")}</div>`;
  }
  if (typeof value === "object") {
    const skip = ["sources_list", "source_mode", "sources_used"];
    const entries = Object.entries(value as Record<string, unknown>).filter(
      ([k, v]) => !skip.includes(k) && v !== null && v !== undefined && v !== ""
    );
    if (!entries.length) return "";
    if (depth === 0) {
      return entries.map(([k, v]) => {
        const label = k.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
        const rendered = smartRenderValue(v, 1);
        if (!rendered) return "";
        return `<div class="pdf-field-block"><h3 class="pdf-field-label">${esc(label)}</h3>${rendered}</div>`;
      }).join("");
    }
    return `<div class="pdf-nested">${entries.map(([k, v]) => {
      const label = k.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
      const rendered = smartRenderValue(v, depth + 1);
      if (!rendered) return "";
      return `<div class="pdf-nested-field"><span class="pdf-nested-label">${esc(label)}:</span> ${rendered}</div>`;
    }).join("")}</div>`;
  }
  return "";
}

function buildGenericSection(title: string, data: string | null): string {
  if (!data) return "";
  const parsed = safeParseJson(data);
  const body = parsed ? smartRenderValue(parsed) : `<p class="pdf-text">${esc(data)}</p>`;
  if (!body) return "";
  return `<section class="pdf-section"><h2 class="pdf-section-title">${esc(title)}</h2><div class="pdf-section-body">${body}</div></section>`;
}

